    private void btnmodificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnmodificarMouseClicked
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(this, "Desea modificar el registro?","Titulo",
            JOptionPane.YES_NO_OPTION,JOptionPane.INFORMATION_MESSAGE)==0){
        trabajadores prod = new trabajadores();

        //cargar el objeto con datos
        prod.setCodigo(Integer.parseInt(txtcodigo.getText()));
        prod.setNombres(txtnombres.getText());
        prod.setSalario_hora(Double.parseDouble(txtsalario.getText()));
        prod.setCantidad_horas_trabajadas(Integer.parseInt(txthoras.getValue().toString()));

        //hacer la modificacion en la lista
        //buscar el index que se va modificar
        int index =0;
        for(trabajadores p : datos){
            if(p.getCodigo() == prod.getCodigo()){
                break;
            }
            index++;
        }
        //hacer la modificacion
        datos.set(index, prod);

        cargar();
        limpiar();
        retotalizar();
        }
    }//GEN-LAST:event_btnmodificarMouseClicked

    private void btneliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btneliminarMouseClicked
        // TODO add your handling code here:

        if(JOptionPane.showConfirmDialog(this, "Desea eliminar el registro?","Titulo",
            JOptionPane.YES_NO_OPTION,JOptionPane.INFORMATION_MESSAGE)==0){

        trabajadores prod = new trabajadores();

        //cargar el objeto con datos
        prod.setCodigo(Integer.parseInt(txtcodigo.getText()));
        prod.setNombres(txtnombres.getText());
        prod.setSalario_hora(Double.parseDouble(txtsalario.getText()));
        prod.setCantidad_horas_trabajadas(Integer.parseInt(txthoras.getValue().toString()));

        //buscar el index que se va modificar
        int index =0;
        for(trabajadores p : datos){
            if(p.getCodigo() == prod.getCodigo()){
                break;
            }
            index++;
        }

        datos.remove(index);
        }
        cargar();
        retotalizar();
        limpiar();

    }//GEN-LAST:event_btneliminarMouseClicked

