     public void cargar(){
           //cargar la lista de modelo, luego el modelo a la tabla 
        modelo.setNumRows(0);//limpiando el modelo
       // metiendo los datos al modelo
       for(trabajadores p : datos){
           
            modelo.addRow(new Object[] {p.getCodigo(),p.getNombres(),
             p.getSalario_hora(), p.getCantidad_horas_trabajadas()});
            
             //aplicar el modelo a la tabla
        tbldatos.setModel(modelo);
        
        total += (Integer.parseInt(txthoras.getValue().toString()))*
                (Double.parseDouble(txtsalario.getText()));
        
        lbltotal.setText(String.valueOf(total));
           
       }   
    }  
     public void limpiar(){
        txtcodigo.setText("");
        txtnombres.setText("");
        txtsalario.setText("");
        txthoras.setValue(0);
    }
     
     public void retotalizar(){
        total =0;
        for(int i=0; i < tbldatos.getRowCount(); i++){
            total += (Double.parseDouble(tbldatos.getValueAt(i, 2).toString() ))*
                    (Integer.parseInt(tbldatos.getValueAt(i, 3).toString()));             
        }
        lbltotal.setText(String.valueOf(total));  
    }
     
     
